vector <ll> get_divisor(ll n){
    vector <ll> ans;
    for(ll i = 1; i * i <= n; i ++){
        if(n % i == 0){
            ans.push_back(i);
            if(i * i == n) continue;
            ans.push_back(n / i);
        }
    }
    sort(ans.begin(), ans.end());
    return ans;
}

